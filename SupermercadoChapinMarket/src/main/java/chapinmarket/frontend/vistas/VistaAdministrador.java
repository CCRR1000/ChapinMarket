    public VistaAdministrador(Empleado empleado) {
        this();
        this.empleado = empleado;
        jLabelUsuario.setText(empleado.getNombre()+" "+empleado.getApellido());
        listaSucursales = new ControlSucursal().generarListaSucursales();
        listaRoles = Rol.values();
        inicializarTabla();
        llenarTabla();
        inicializarCombos();
    }
    
    private void inicializarCombos() {
        this.jComboBoxSucursal.addItem("");
        for (int i = 0; i < listaSucursales.size(); i++) {
            this.jComboBoxSucursal.addItem(listaSucursales.get(i).getNombre());
        }
        
        for (int i = 0; i < listaRoles.length; i++) {
            this.jComboBoxRol.addItem(listaRoles[i].name());
        }
    }
    
    public void inicializarTabla() {
        
        modeloTabla.addColumn("Usuario");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Apellido");
        modeloTabla.addColumn("Rol");
        modeloTabla.addColumn("Sucursal");
        modeloTabla.addColumn("CUI");
        modeloTabla.addColumn("Salario");
        modeloTabla.addColumn("Numero Caja");

        this.jTable.setModel(modeloTabla);
    }

    public void llenarTabla() {
        ControlEmpleado.mostrarRegistros(modeloTabla);
    }
    
    public void actualizarTabla() {
        int filas = modeloTabla.getRowCount();
        for (int i = 0; i < filas; i++) {
            modeloTabla.removeRow(0);
        }
        llenarTabla();
    }
    

