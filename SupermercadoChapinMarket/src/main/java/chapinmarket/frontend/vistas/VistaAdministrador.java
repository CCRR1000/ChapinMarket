    private int getIndexRol(Rol rol) {
        for (int i = 0; i < this.listaRoles.length; i++) {
            if (listaRoles[i].name().equals(rol.name())) {
                return i;
            }
        }
        return 0;
    }
    
    private int getIndexSucursal(Sucursal sucursal) {
        for (int i = 0; i < listaSucursales.size(); i++) {
            if (listaSucursales.get(i).equals(sucursal)) {
                return i-1;
            }
        }
        return 0;
    }


