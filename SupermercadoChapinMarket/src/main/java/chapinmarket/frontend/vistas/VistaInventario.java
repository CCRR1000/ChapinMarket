    private void jButtonConsultarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarProductoActionPerformed
        
        String codigo = JOptionPane.showInputDialog("Ingrese el codigo del producto:");
        habilitarPanelDatos(true);
        
        if (codigo != null) {
            //Producto producto = ControlProducto.getProductoPorCodigo(codigo);
            Existencia existencia = ControlExistencia.getExistenciaPorIDS(codigo, empleado.getSucursal().getId());
            if (existencia==null) {
                JOptionPane.showMessageDialog(null, "No se encontró el producto con el código ingresado","Codigo incorrecto",JOptionPane.ERROR_MESSAGE);
                habilitarPanelDatos(false);
            } else {
                llenarCamposDatos(existencia.getProducto(), existencia.getCantidadBodega());
            }
        } else {
            habilitarPanelDatos(false);
        }
    }//GEN-LAST:event_jButtonConsultarProductoActionPerformed

