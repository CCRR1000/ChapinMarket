    private void llenarCamposDatos(Producto producto, int cantidadBodega) {
        jTextFieldCodigoBarras.setText(producto.getCodigo());
        jTextFieldCodigoBarras.setEditable(false);
        jTextFieldNombre.setText(producto.getNombre());
        jTextFieldNombre.setEditable(false);
        jTextFieldMarca.setText(producto.getMarca());
        jTextFieldMarca.setEditable(false);
        jTextAreaDescripcion.setText(producto.getDescripcion());
        jTextAreaDescripcion.setEditable(false);
        
        jTextFieldPrecio.setText(String.valueOf(producto.getPrecio()));
        jTextFieldPrecio.setEditable(false);
        jTextFieldCantidadBodega.setText(String.valueOf(cantidadBodega));
        jTextFieldCantidadBodega.setEditable(false);
    }
    
    
    
